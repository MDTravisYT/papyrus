HIMOVE	=	3*2	;	anything above this errors

SETMOVE:
		MOVE.L	#MAP_PAPHEAD,	OBJ.MAP(A0)
		MOVE.W	#$200,		OBJ.TILE(A0)
		MOVE.B	#4,			OBJ.RENDERFLAGS(A0)
		MOVE.B	#2,			OBJ.PRIORITY(A0)

		MOVEQ	#0,	D0
		CMPI.B	#HIMOVE,	MOVENUM
		BEQ.W	MoveErr
		MOVE.B	MOVENUM,OBJ.ACTION(A0)
		MOVE.B	OBJ.ACTION(A0),	D0
		MOVE.W	@INDEX(PC,D0.W),	D1
		JMP		@INDEX(PC,D1.W)
		
	@INDEX:
		DC.W	Move1-@INDEX
		DC.W	Move2-@INDEX
		DC.W	Move3-@INDEX
		@INDEX_END:
		
	Move1:
	
		MOVEQ	#0,	D0
		MOVE.B	OBJ.SUBACTION(A0),	D0
		MOVE.W	@MV1_INDEX(PC,D0.W),	D1
		JMP		@MV1_INDEX(PC,D1.W)
		
		@MV1_INDEX:
			DC.W	@R1-@MV1_INDEX
			DC.W	@R2-@MV1_INDEX
		@R1:
			TEXTSETUP		$48140002,TXT_CHECK2,$A3,$180
			ADDI.B	#2,	OBJ.SUBACTION(A0)
		@R2:
			JSR		CHKABC
			beq.w	@end
			MOVE.B	#$A0,	D0
			JSR		PlaySound
			JSR		SPAWNSOUL
			MOVE.W	#60*10,	TIMER
			LEA		BoneSpawn1,A1
			JSR		SpawnObjList
			STOPTXT
			COPYTILEMAP		BOARDMAP,	$8700,	39,	7
			rts
		@end:
			TST.W	TEXTPROG
			BEQ.S	@skip
			LEA		ANIM_PAPHEAD,	A1
			JSR		_OBJECTANIMATE
		@skip:
			JMP		_OBJECTDISPLAY

	Move2:
	;	BSET	#0,	BLUEMODE
		MOVEQ	#0,	D0
		MOVE.B	OBJ.SUBACTION(A0),	D0
		MOVE.W	@MV2_INDEX(PC,D0.W),	D1
		JMP		@MV2_INDEX(PC,D1.W)
		
		@MV2_INDEX:
			DC.W	@R1-@MV2_INDEX
			DC.W	@R2-@MV2_INDEX
		@R1:
			TEXTSETUP		$48140002,TST3,$A3,$180
			
			ADDI.B	#2,	OBJ.SUBACTION(A0)
		@R2:
			JSR		CHKABC
			beq.w	@end
			MOVE.B	#$A0,	D0
			JSR		PlaySound
			JSR		SPAWNSOUL
			MOVE.W	#60*10,	TIMER
			LEA		BoneSpawn2,A1
			JSR		SpawnObjList
			STOPTXT
			COPYTILEMAP		BOARDMAP,	$8700,	39,	7
		@end:
			TST.W	TEXTPROG
			BEQ.S	@skip
			LEA		ANIM_PAPHEAD,	A1
			JSR		_OBJECTANIMATE
		@skip:
			JMP		_OBJECTDISPLAY
		
	MoveErr:
		MOVEQ	#0,	D0
		MOVE.B	OBJ.SUBACTION(A0),	D0
		MOVE.W	@MVERR_INDEX(PC,D0.W),	D1
		JMP		@MVERR_INDEX(PC,D1.W)
		
		@MVERR_INDEX:
			DC.W	@R1-@MVERR_INDEX
			DC.W	@R2-@MVERR_INDEX
		@R1:
			TEXTSETUP	$48080002,TXTERR,$A1,$100
			ADDI.B	#2,	OBJ.SUBACTION(A0)
		@R2:
			RTS
			
	Move3:
		MOVEQ	#0,	D0
		MOVE.B	OBJ.SUBACTION(A0),	D0
		MOVE.W	@MV3_INDEX(PC,D0.W),	D1
		JMP		@MV3_INDEX(PC,D1.W)
		
		@MV3_INDEX:
			DC.W	@R1-@MV3_INDEX
			DC.W	@R2-@MV3_INDEX
			DC.W	@R3-@MV3_INDEX
			DC.W	@R4-@MV3_INDEX
		@R1:
			TEXTSETUP_BLUE		$481C0002,TXT_BLUE1,$A3,$8180
			
			ADDI.B	#2,	OBJ.SUBACTION(A0)
			MOVE.W	#90,	TIMER
		@R2:
			JSR		CHKABC
			beq.w	@end
			ADDI.B	#2,	OBJ.SUBACTION(A0)
			TEXTSETUP_BLUE		$481C0002,TXT_BLUE2,$A3,$8180
			MOVE.B	#0,	TIMER
			RTS
		@R3:
			JSR		CHKABC
			beq.w	@end
			ADDI.B	#2,	OBJ.SUBACTION(A0)
			TEXTSETUP_BLUE		$481C0002,TXT_BLUE3,$A3,$8180
			MOVE.B	#0,	TIMER
			RTS
		@R4:
			JSR		CHKABC
			beq.w	@end
			MOVE.W	#152,	OBJ.X+(OBJSLOT00)
			MOVE.W	#204,	OBJ.Y+(OBJSLOT00)
			MOVE.B	#0,	OBJ.ACTION+(OBJSLOT00)
			MOVE.B	#0,	OBJ.FRAME+(OBJSLOT00)
			MOVE.B	#3,	OBJ.ID+(OBJSLOT00)
			ADDI.B	#2,	MOVENUM
			TEXTSETUP	$48080002,TXT_BLUE_IDLE,$A2,$100
		@end:
			RTS
			
CHKABC:
		MOVEQ	#0,	D0
		MOVEQ	#0,	D1
		MOVE.B	JOYPADPRESS,	D0
		andi.b	#btnA+btnB+btnC,D0
		RTS
			
SPAWNSOUL:
		MOVE.B	#1,	OBJ.ID(A0)
		MOVE.W	#160,	OBJ.X(A0)
		MOVE.W	#140,	OBJ.Y(A0)
		MOVE.B	#0,		OBJ.ACTION(A0)
		MOVE.B	#0,		OBJ.FRAME(A0)
		MOVE.W	#0,		OBJ.YSPEED(A0)
		RTS
			
SpawnObjList:	;	Expects input of a list into A1
		LEA		(OBJSLOT01),A2
	@loop:
		MOVE.B	(A1)+,OBJ.ID(A2)
		MOVE.B	(A1)+,OBJ.ARGUMENT(A2)
		MOVE.W	(A1)+,OBJ.X(A2)
		MOVE.W	(A1)+,OBJ.Y(A2)
		MOVE.W	(A1)+,OBJ.XSPEED(A2)
		CMPI.B	#$FF,(A1)
		BEQ.S	@ret
		LEA		OBJ.SIZE(A2),A2
		jmp		@loop
	@ret:
		rts
			
