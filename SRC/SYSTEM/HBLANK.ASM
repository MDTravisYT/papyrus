HBLANK:
		TST.W	HBLANKFLAG
		BEQ.S	.EXIT
		MOVEM.L	D0/A0/A5,-(SP)
		MOVE.W	#0,	HBLANKFLAG
		MOVE.W	#$8405,	(VDPCTRL).L
		MOVE.W	#$857C,	(VDPCTRL).L
		MOVE.L	#$78000003,	(VDPCTRL).L
		LEA		SPRITETABLE,	A0
		LEA		(VDPDATA).L,	A5
		MOVE.W	#$9F,	D0
		
	.SPRITE:
		MOVE.L	(A0)+,	(A5)
		DBF		D0,	.SPRITE
		MOVEM.L	(SP)+,	D0/A0/A5
		
	.EXIT:
		RTE