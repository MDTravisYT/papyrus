MAIN:
		JSR		CLEARSCREEN
		
		move.l	#VRAMWRITE,	(VDPCTRL)
		LEA		BATTLEART,		A0
		LEA		VDPDATA,	A1
		MOVE.W	#(BATTLEART_END-BATTLEART)/2-1,		D0
	@LOADGFX:
		MOVE.W	(A0)+,	(A1)
		DBF		D0,	@LOADGFX
		
		move.l	#VRAMWRITE,	(VDPCTRL)
		LEA		TXTART,		A0
		LEA		VDPDATA,	A1
		WRITEVRAM	$2000
		MOVE.W	#(TXTART_END-TXTART)/2-1,		D0
	@LOADGFX2:
		MOVE.W	(A0)+,	(A1)
		DBF		D0,	@LOADGFX2
		
		LEA		BATTLEPAL,	A0
		LEA		PALFADEBUFFER,	A1
		MOVE.B	#16,	D0
	@PALLOOP:
		MOVE.L	(A0)+,	(A1)+
		DBF		D0,	@PALLOOP
		
	COPYTILEMAP		BATTLEMAP,	$8084,	35,	25
	
		MOVE.B	#$03,	(OBJSLOT00)
		LEA		(OBJSLOT00),	A0
		MOVE.W	#152,	OBJ.X(A0)
		MOVE.W	#204,	OBJ.Y(A0)
		
		JSR		PALFADEIN
		
		MOVE.L	#$48080002,	TEXTLOG
		MOVE.W	#$0,	TEXTPROG
		
	@loop:
		MOVE.B	#1,	(VBLANKCMD).W
		jsr		VSYNC
		JSR		RUNOBJECTS
		JSR		PROCESSSPRITES
		
		BTST	#0,	TIMER+1
		BEQ.S	@skipDialogue
		LEA		TST,	A0
		ADD.W	TEXTPROG,	A0
		CMPI.B	#1,	(A0)
		BNE.S	@no_newline
		MOVE.L	#$49080002,	TEXTLOG
		ADDI.W	#1,	TEXTPROG
		bra.w	@loop
	@no_newline:
		CMPI.B	#$FF,	(A0)
		BNE.S	@noEnd
		bra.w	@loop
	@noEnd:
		MOVE.L	TEXTLOG,	(VDPCTRL)
		MOVE.W	#$100,	D0
		ADD.B	(A0)+,	D0
		MOVE.W	D0,	($C00000)
		add.l	#$20000,TEXTLOG
		add.w	#1,	TEXTPROG
		cmpi.b	#' ',	D0
		beq.s	@skipDialogue
		move.b	#$A2,	D0
		jsr		PlaySound
	@skipDialogue:
		bra.w	@loop
		rts
		
BATTLEPAL:	INCBIN		"PAL/BATTLE.PAL"
BATTLEMAP:	INCBIN		"MAP/BATTLE.MAP"
BOARDMAP:	INCBIN		"MAP/BOARD.MAP"
BATTLEART:	INCBIN		"ART/BATTLE.UNC"
	BATTLEART_END:
TXTART:	INCBIN		"ART/FONT.UNC"
	TXTART_END:

TST:
	DC.B	'Boner.',1,'This should a new line!',$FF
	TSTEND:
	even